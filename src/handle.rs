use axum::{
    extract::State,
    http::StatusCode,
};
use sqlx::postgres::PgPool;
use tracing::{debug, info};

pub(crate) async fn create_account(
    State(pool): State<PgPool>,
) -> Result<String, (StatusCode, String)> {
    sqlx::query_scalar("select 'hello world from pg'")
        .fetch_one(&pool)
        .await
        .map_err(internal_error)
}
